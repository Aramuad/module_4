package lab_6;

public class EvenOdd {

	public static void main(String[] args) {

		System.out.println();
		System.out.print("Even numbers between 50 and 100: ");
		int n = 50;
			while (n < 100) {
				
				System.out.print(n + ", ");
				n += 2;
				
			}
			
		System.out.print("100.");
		
		System.out.println();
		System.out.print("Odd numbers between 50 and 100: ");
		int i = 51;
			while (i < 99) {
				
				System.out.print(i + ", ");
				i += 2;
				
			}
			
		System.out.print("99.");
		System.out.println();
		System.out.println();
		
	}

}
